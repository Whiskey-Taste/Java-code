package com.itheima.note;
/**
    目标：学会使用注释
    这个程序就是讲解注释的
    这是文档注释
 */
public class NoteDemo {
    public static void main(String[] args) {
        // 这是一行单行注释
        // 以下是一行打印语句，是往控制台传输内容的
        System.out.println("开始学习Java，好嗨皮");

        /*
            这是多行注释
            可以写一首诗
         */
        System.out.println("注释下面");
        System.out.println("可以继续打印");
    }
}
